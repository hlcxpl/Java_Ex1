public class ejercicio4 {

    public static void main(String[] args) {
        double a = 10;
        double b = 20;
        double c = 30;

        double promedioAritmetico = (a + b + c) / 3;

        double promedioArmonico = 3 / (1 / a + 1 / b + 1 / c);

        System.out.println("Promedio aritmético: " + promedioAritmetico);
        System.out.println("Promedio armónico: " + promedioArmonico);

    }
}
